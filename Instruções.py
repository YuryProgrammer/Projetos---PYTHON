# 1. Entenda o que precisa fazer
# Seu programa precisa mostrar um menu com opções para o usuário.

# Deve permitir adicionar tarefas, ver tarefas, marcar como concluída, remover tarefas e sair.

# Cada tarefa pode ser representada como um objeto simples (por exemplo, um dicionário) com nome e status.

# 2. Comece pequeno e vá construindo
# Primeiro, faça só o menu aparecer e peça para o usuário escolher uma opção.

# Depois, implemente a função para adicionar tarefas (peça o nome, salve numa lista).

# Depois, faça a função que mostra todas as tarefas.

# Vá implementando as outras funções uma a uma.

# 3. Use listas e dicionários
# Armazene as tarefas numa lista global.

# Cada tarefa é um dicionário com duas chaves: "nome" e "concluida".

# Exemplo: {"nome": "Comprar leite", "concluida": False}

# 4. Controle o fluxo com um loop
# Use um while True: para mostrar o menu repetidamente até o usuário escolher sair.

# Para cada opção do menu, execute a função correspondente.

# 5. Pense nos detalhes
# Como mostrar a lista com número para o usuário (para selecionar tarefas depois)?

# Como alterar o status da tarefa (marcar como concluída)?

# Como remover uma tarefa da lista?

# 6. Teste sempre
# Depois de implementar cada função, rode seu programa para ver se funciona.

# Se der erro, leia a mensagem com calma e tente entender o que aconteceu.

# 7. Não tenha medo de usar print para debugar
# Se alguma coisa não funcionar, coloque prints para ver o que está acontecendo dentro das funções.

# 8. Dicas extras para o futuro
# Depois que o básico funcionar, pense em salvar as tarefas num arquivo para não perder quando fechar o programa.

